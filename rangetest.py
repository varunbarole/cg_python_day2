# for with range
for val in range(1,11):
    print(val,end= " ")

# for with list
alist = [10,20,30,40]
for val in alist:
    print(val)

# for with string
for char in "python":
    print(char)

# for with dictionary
adict = {"chap1":10 ,"chap2":20}
for key in adict:
    print("Key  :", key)
    print("Value:", adict[key])

# for with set
aset = {10,10,20,30}
for val in aset:
    print(val)


